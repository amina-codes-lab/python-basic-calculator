# Conversions

#float=>int
b=100.99
print(b)
print(type(b))
a=int(b)
print(a)
print(type(a))

#int=>float
b=1100
print(b)
print(type(b))
a=float(b)
print(a)
print(type(a))

#str=>int
num="224466"
print(type(num))
n=int(num)
print(n)
print(type(n))

#int=>str
num1=2334
print(type(num1))
str=str(num1)
print(type(str))

#int=>bool
isFalse=False
print(type(isFalse))
bool=int(isFalse)
print(bool)
print(type(bool))

